	//wizchip_cris_initialize();
	wizchip_reset();
	wizchip_initialize();
	wizchip_check();
  
  network_initialize(g_net_info);
  print_network_information(g_net_info);
  
  eMBRTUInit(huart1.Init.BaudRate);
  HAL_UART_Receive_IT(&huart1, &uartRxByte, 1);
  
